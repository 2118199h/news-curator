# ========================================
# ng_words.py — NGワードリスト定義
# ========================================
# 記事のタイトルや概要にこれらの単語が含まれていた場合、
# その記事は「関係ない」と判断して自動的に除外する。
# これにより、Gemini APIを呼ぶ前に不要な記事を減らし、コストを節約できる。
#
# 【カスタマイズ方法】
# 不要な記事が混ざる場合は、そのキーワードをリストに追加してください。
# 逆に、必要な記事が除外されてしまう場合は、該当のキーワードを削除してください。
# ========================================


# --- 求人・採用記事を除外するNGワード ---
# 企業名で検索すると採用情報がヒットしがちなので除外する
JOB_WORDS = [
    "求人",
    "採用情報",
    "転職",
    "中途採用",
    "新卒採用",
    "キャリア採用",
    "年収",
    "給与",
    "ワークライフバランス",
    "面接対策",
    "エントリー受付",
    "インターン募集",
    "社員インタビュー",
    "働き方紹介",
]

# --- 広告・PR記事を除外するNGワード ---
# タイアップ記事や広告記事を除外する
AD_WORDS = [
    "[PR]",
    "【PR】",
    "PR TIMES",
    "広告",
    "タイアップ",
    "Sponsored",
    "[AD]",
    "プレゼントキャンペーン",
    "アフィリエイト",
    "お得な情報",
    "期間限定セール",
    "クーポン配布",
    "プロモーション",
]

# --- 低品質・無関係なコンテンツを除外するNGワード ---
# ニュースとは関係のないエンタメ系や品質の低い記事を除外する
LOW_QUALITY_WORDS = [
    "まとめ速報",
    "炎上",
    "芸能ニュース",
    "ゴシップ",
    "占い",
    "心理テスト",
    "性格診断",
    "ランキング形式で紹介",
    "驚きの結果",
    "衝撃の事実",
    "閲覧注意",
]

# --- 商品セール・安売り記事を除外するNGワード ---
# 「スマホが実質24円」のような商品のお買い得情報を除外する
# ※「値下げ」は電気料金値下げなど経済ニュースにも使われるため入れていない
SALE_WORDS = [
    "セール",
    "お得",
    "割引",
    "クーポン",
    "ポイント還元",
    "特価",
    "激安",
    "在庫処分",
    "投げ売り",
    "タイムセール",
    "キャンペーンまとめ",
    "% off",
    "discount",
]

# --- 端末の発売告知・乗り換え割引の記事を除外するNGワード ---
# 「UQ mobileが○○を発売」「MNPで実質47円」のような
# 一般的な発売・販売キャンペーン記事を除外する。
# ※ 料金プラン全体の値下げのような重要ニュースには
#   これらの単語が現れにくいため、そうした記事は残る
DEVICE_DEAL_WORDS = [
    "発売",             # 端末の発売告知
    "予約受付",
    "販売開始",
    "取り扱い開始",
    "MNP",              # 乗り換え割引の定番ワード
    "乗り換えで",
    "先着",             # 先着○名キャンペーン
    "進呈",             # プレゼント進呈
    "特典",
    "認定中古",         # 中古端末の販売情報
    "キャンペーン",     # 各種販促キャンペーン
    "ポイント進呈",
]

# --- 正規表現で除外するパターン ---
# 単語だけでは誤爆する場合に、パターン（文字の並び方）で判定する。
# 例:「実質」だけをNGにすると「実質賃金」「実質GDP」などの
#     経済ニュースまで消えてしまうため、「実質＋金額」の形だけ除外する
NG_PATTERNS = [
    r"実質\s*[0-9０-９,]+\s*円",   # 「実質24円」「実質4万7000円」など
    r"一括\s*[0-9０-９,]+\s*円",   # 「一括1円」など
    r"[0-9０-９,]+\s*円\s*に値下げ",  # 「24円に値下げ」など（商品の値下げ）
    r"月々\s*[0-9０-９,]+\s*円",   # 「月々1円」など
]

# --- 英語記事用のNGワード ---
# 米国ニュースのフィードに混ざるセール情報・広告記事を除外する
ENGLISH_NG_WORDS = [
    "Best deals",       # セール情報
    "best deal",
    "promo code",       # クーポンコード
    "coupon",
    "gift guide",       # ギフト特集
    "giveaway",         # プレゼント企画
    "We're hiring",     # 求人
    "Sponsored post",   # 広告記事
    "Black Friday",     # セール特集
    "Cyber Monday",
]

# --- 全NGワードを1つにまとめたリスト ---
# prefilter.py はこのリストを使ってチェックする
ALL_NG_WORDS = (
    JOB_WORDS
    + AD_WORDS
    + LOW_QUALITY_WORDS
    + SALE_WORDS
    + DEVICE_DEAL_WORDS
    + ENGLISH_NG_WORDS
)
