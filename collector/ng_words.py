# ========================================
# ng_words.py — NGワードリスト定義
# ========================================
# 記事のタイトルや概要にこれらの単語が含まれていた場合、
# その記事は「関係ない」と判断して自動的に除外する。
# これにより、Gemini APIを呼ぶ前に不要な記事を減らし、コストを節約できる。
#
# 【カスタマイズ方法】
# 不要な記事が混ざる場合は、そのキーワードをリストに追加してください。
# 逆に、必要な記事が除外されてしまう場合は、該当のキーワードを削除してください。
# ========================================


# --- 求人・採用記事を除外するNGワード ---
# 企業名で検索すると採用情報がヒットしがちなので除外する
JOB_WORDS = [
    "求人",
    "採用情報",
    "転職",
    "中途採用",
    "新卒採用",
    "キャリア採用",
    "年収",
    "給与",
    "ワークライフバランス",
    "面接対策",
    "エントリー受付",
    "インターン募集",
    "社員インタビュー",
    "働き方紹介",
]

# --- 広告・PR記事を除外するNGワード ---
# タイアップ記事や広告記事を除外する
AD_WORDS = [
    "[PR]",
    "【PR】",
    "PR TIMES",
    "広告",
    "タイアップ",
    "Sponsored",
    "[AD]",
    "プレゼントキャンペーン",
    "アフィリエイト",
    "お得な情報",
    "期間限定セール",
    "クーポン配布",
    "プロモーション",
]

# --- 低品質・無関係なコンテンツを除外するNGワード ---
# ニュースとは関係のないエンタメ系や品質の低い記事を除外する
LOW_QUALITY_WORDS = [
    "まとめ速報",
    "炎上",
    "芸能ニュース",
    "ゴシップ",
    "占い",
    "心理テスト",
    "性格診断",
    "ランキング形式で紹介",
    "驚きの結果",
    "衝撃の事実",
    "閲覧注意",
]

# --- 商品セール・安売り記事を除外するNGワード ---
# 「スマホが実質24円」のような商品のお買い得情報を除外する
# ※「値下げ」は電気料金値下げなど経済ニュースにも使われるため入れていない
SALE_WORDS = [
    "セール",
    "お得",
    "割引",
    "クーポン",
    "ポイント還元",
    "特価",
    "激安",
    "在庫処分",
    "投げ売り",
    "タイムセール",
    "キャンペーンまとめ",
    "% off",
    "discount",
]

# --- 英語記事用のNGワード ---
# 米国ニュースのフィードに混ざるセール情報・広告記事を除外する
ENGLISH_NG_WORDS = [
    "Best deals",       # セール情報
    "best deal",
    "promo code",       # クーポンコード
    "coupon",
    "gift guide",       # ギフト特集
    "giveaway",         # プレゼント企画
    "We're hiring",     # 求人
    "Sponsored post",   # 広告記事
    "Black Friday",     # セール特集
    "Cyber Monday",
]

# --- 全NGワードを1つにまとめたリスト ---
# prefilter.py はこのリストを使ってチェックする
ALL_NG_WORDS = JOB_WORDS + AD_WORDS + LOW_QUALITY_WORDS + SALE_WORDS + ENGLISH_NG_WORDS
